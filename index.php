<script>
function displayFile(file, place) {
  document.getElementById(place).innerHTML = file;
}

function getFile(callback, file, place) {
  let xhttp = new XMLHttpRequest();
  xhttp.open('GET', file);
  xhttp.onload = function() {
    if (xhttp.status === 200) {
      callback(this.responseText, place);
    } else {
      callback("Error" + xhttp.status, place);
    }
  }
  xhttp.send();
}

